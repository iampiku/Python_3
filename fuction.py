# we can pass multiple parameter in a fuction
# parameter are the name and age
def greet_user(name, age):
    print(f"Hello my name is {name}, I am {age} year old")

# Pradipta  Chatterjee and 22  are the arguments provided to the fuction
greet_user("Pradipta Chatterjee", age="22")
# The age is called the keyword argument with this position of the argument provided does'nt matter